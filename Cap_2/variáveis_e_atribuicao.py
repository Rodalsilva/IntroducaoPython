# O interpretador Python é um programa que aceita comandos escritos em Python e os executa linha a linha.
# Ou seja, ele traduz nossos programas em um formato que pode ser executado pelo computador.
# Em programação, variáveis são utilizadas para armazenar valores e para dar nome a uma área de memória do computador onde armazenamos dados.
# Podemos imaginar a memória do computador como um grande armário, em que cada gaveta tem um nome.
# Para armazenar algo nessas gavetas, usamos o símbolo de igualdade entre o nome da gaveta e o valor que queremos armazenar.
# A operação acima é chamada de atribuição.

# Exercício 2.6 - Cálculo de aumento de salário

salário = 750 # Atribuição do valor 750 à variável salário.
aumento = 15  # Atribuição do valor 15 à variável aumento
print(salário + (salário*aumento/100)) # A função print realiza a impressão, isto é, exibe na tela o resultado calculado da operação.
